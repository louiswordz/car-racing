# Function & Strings

def Story():
    print("I can tell a story!")
    
Story()

def User():
    print("Good morning everyone!")
    
User()

def username(name):
    print("my name is " + name )
    
username("Blessing")

def classmate(fname, lname):
    print("How are you " + fname.upper() + " " + lname.title())
    
classmate("John", "brown")

def Running(name):
    print(name + " is running!")
    
Running("Paul")

def Eating(fname, lname):
    print(fname + " and " + lname + " is eating")
    
Eating("Bassey", "Gareth")
    